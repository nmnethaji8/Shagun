    PT(4)=ZQ
  ELSEIF(MBA.EQ.9)THEN
    WRITE(8,*)'[ERR] NOT CODED FOR BASEFNC QUAD'
    STOP
    ! THE FOLLOWING IS WRONG
    ! PT(1)=1.0
    ! PT(2)=COORX(NI)
    ! PT(3)=COORY(NI)
    ! PT(4)=COORZ(NI)
    ! PT(4)=PB2(2)**2
    ! PT(5)=PB2(2)*COORX(NI)
    ! PT(6)=PB2(3)**2
  ENDIF

END SUBROUTINE BASEFUN_SHA
