!!------------------------- WEIGHTF1_SHA --------------------------!!
SUBROUTINE WEIGHTF1_SHA(LNODE,NLMAX,NODN,KW,R,R0,CC,NODEID,NWALLID,&
  COORX,COORY,COORZ,INOD,XQ,YQ,ZQ,ILINK,NN,ND,W,IDWEI,IDSHAPE,&
